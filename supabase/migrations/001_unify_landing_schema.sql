-- ─── 4. Solicitudes de contratación (leads) ─────────────────────────
CREATE TABLE IF NOT EXISTS landing.requests (
  id            uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  user_id       uuid REFERENCES auth.users(id) ON DELETE SET NULL,
  service_id    bigint REFERENCES landing.services(id),
  contact_name  text NOT NULL,
  contact_email text NOT NULL,
  contact_phone text,
  message       text,
  status        text CHECK (status IN ('pending', 'contacted', 'resolved')) DEFAULT 'pending',
  created_at    timestamptz DEFAULT now()
);

GRANT ALL ON landing.requests TO anon, authenticated, service_role;

ALTER TABLE landing.requests ENABLE ROW LEVEL SECURITY;

-- Cualquiera (incluso visitante anónimo) puede crear una solicitud
DROP POLICY IF EXISTS "landing_requests_insert_anyone" ON landing.requests;
CREATE POLICY "landing_requests_insert_anyone" ON landing.requests
  FOR INSERT WITH CHECK (true);

-- Un usuario logueado ve sus propias solicitudes (por user_id si se
-- seteó, o por email si la mandó como visitante y coincide con su
-- cuenta actual)
DROP POLICY IF EXISTS "landing_requests_select_own" ON landing.requests;
CREATE POLICY "landing_requests_select_own" ON landing.requests
  FOR SELECT TO authenticated
  USING (
    auth.uid() = user_id
    OR contact_email = (auth.jwt() ->> 'email')
  );

-- Admins/superadmins de NMF ven y gestionan todas las solicitudes
DROP POLICY IF EXISTS "landing_requests_admin_all" ON landing.requests;
CREATE POLICY "landing_requests_admin_all" ON landing.requests
  FOR ALL TO authenticated
  USING (public.get_user_role() IN ('admin', 'superadmin'))
  WITH CHECK (public.get_user_role() IN ('admin', 'superadmin'));

-- Recargar caché de PostgREST
NOTIFY pgrst, 'reload schema';
