-- Crear tabla requests (solicitudes de contratación)
create table public.requests (
  id uuid default gen_random_uuid() primary key,
  user_id uuid references auth.users, -- Permitiremos nulo por ahora para visitantes anónimos
  service_id bigint references services,
  contact_name text not null,
  contact_email text not null,
  contact_phone text,
  message text,
  status text check (status in ('pending', 'contacted', 'resolved')) default 'pending',
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Configurar RLS para requests
alter table public.requests enable row level security;
create policy "Anyone can insert a request." on requests for insert with check (true);
create policy "Users can view their own requests." on requests for select using (auth.uid() = user_id);
create policy "Admins can view and update all requests." on requests for all using (
  exists (select 1 from profiles where id = auth.uid() and role = 'admin')
);
