-- Crear tabla profiles para usuarios/admin
create table public.profiles (
  id uuid references auth.users not null,
  role text check (role in ('admin', 'client')) default 'client',
  full_name text,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  primary key (id)
);

-- Configurar RLS para profiles
alter table public.profiles enable row level security;
create policy "Public profiles are viewable by everyone." on profiles for select using (true);
create policy "Users can insert their own profile." on profiles for insert with check (auth.uid() = id);
create policy "Users can update own profile." on profiles for update using (auth.uid() = id);

-- Crear tabla services
create table public.services (
  id bigint generated by default as identity primary key,
  name text not null,
  description text,
  price text,
  features text[], -- Array de características
  is_active boolean default true,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Configurar RLS para services
alter table public.services enable row level security;
create policy "Services are viewable by everyone." on services for select using (true);
create policy "Only admins can insert/update services." on services for all using (
  exists (select 1 from profiles where id = auth.uid() and role = 'admin')
);

